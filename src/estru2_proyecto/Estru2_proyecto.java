                if (registros >= 10) {
                    for (int i = 0; i < 10; i++) {
                        ArrayList<Object> temp = archivo1_principal.LoadRegistro(i).getData();
                        model.addRow(temp.toArray());
                    }
                } else {
                    for (int i = 0; i < registros; i++) {
                        ArrayList<Object> temp = archivo1_principal.LoadRegistro(i).getData();
                        model.addRow(temp.toArray());
                    }
                }
